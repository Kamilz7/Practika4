            InitializeComponent();
        }
        private void CalculateButton_Click(object sender, RoutedEventArgs e)
        {
            int month = int.Parse(MonthTextBox.Text);
            int days = 0;
            switch (month)
            {
                case 2:
                    days = 28;
                    break;
                case 4:
                case 6:
                case 9:
                case 11:
                    days = 30;
                    break;
                default:
                    days = 31;
                    break;
            }
            MessageBox.Show($"Количество дней в месяце {month} является {days}.");
        }
    }
